package com.wma.gestorFinanceiro.domain.enums;

/**
 * Enum para representar o tipo de uma transação financeira.
 */
public enum TransactionType {
    /**
     * Representa uma entrada de dinheiro (ex: salário, rendimento extra).
     */
    RECEITA,

    /**
     * Representa uma saída de dinheiro (ex: aluguer, compra).
     */
    DESPESA
}
